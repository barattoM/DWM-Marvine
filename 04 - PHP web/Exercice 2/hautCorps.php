            <h3>Le cookie de la semaine</h3>
        </div>
        <div class="descriptionActu">
            <div class="ingredients">
                ingredients:
                <ul>
                    <li>170g de farine</li>
                    <li>80g de sucre ;</li>
                    <li>80g de beurre ;</li>
                    <li>1 tablette de chocolat blanc ;</li>
                    <li>1 demi sachet de levure chimique ;</li>
                    <li>1 bouchon d’arôme de vanille ;</li>
                    <li>1 œuf ;</li>
                    <li>1 petite poignée de framboises.</li>
                </ul>
            </div>
            <div class="photoActu">
                <img src="./cookies-chocolat-blanc.jpg" alt="cookieSemaine">
            </div>
        </div>
        <div class="recette">
            Mettre le four à chauffer à 180°.
            Faire fondre le beurre et l’incorporer au sucre. Rajouter l’œuf et mélanger.
            Ajouter l’arôme de vanille ainsi que la levure, puis, petit à petit, verser la farine en remuant
            avec une cuillère en bois.
            Couper le chocolat blanc pour en faire des petites pépites et couper les framboises en 4.
            Ajouter les pépites de chocolat blanc dans la pâte.
            La pâte va vous sembler bien collante et vous aurez envie de rajouter de la farine.
            Vous pouvez le faire, mais n’en abusez pas ! Sinon vos cookies seront moins moelleux et beaucoup
            plus épais. Donc même si la pâte colle un peu, c’est mieux !
            Faire des petits tas de pâte et ajouter dans chaque, 3 ou 4 morceaux de framboises.
            Laisser cuire 10min à 180°.

